glossary = {
	'print': 'prints out message',
	'title': 'changes every word in sentence to capital',
	'rstrip': 'removes extra spaces from the right side of sentence',
	'lstrip': 'removes extra spaces from the left side of sentence',
	'append': 'appends the value at the end of the list',
	}
	
for key, value in glossary.items():
	print("\n" + key + ": " + value + ".")
